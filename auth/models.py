from pydantic import BaseModel


class Token(BaseModel):
    access_token: str
    token_type: str


class TokenData(BaseModel):
    username: str = None


class User(BaseModel):
    username: str
    email: str
    full_name: str
    pesel: str
    disabled: bool = False


class UserRegister(User):
    plain_password: str


class UserInDB(User):
    hashed_password: str


